ddf.compare.aics <- function(ddf.output.list){

    aic.table <- sapply(ddf.output.list, function(x) summary(x)$aic) ; aic.table
    
    aic.table <- cbind(AIC = aic.table, deltaAIC = aic.table - min(aic.table)) ; aic.table
    
    return(aic.table)

}

