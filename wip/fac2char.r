# Convert all factors in a data frame to character variables

################################################################################
################################################################################

fac2char <- function(data.frame){
    
    for(i in 1:ncol(data.frame))
        
        if(class(data.frame[,i])=="factor")
            
            data.frame[,i] <- as.character(data.frame[,i])
    
    
    return(data.frame)    
    
}

