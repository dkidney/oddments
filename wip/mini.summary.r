mini.summary <- function(est=NULL, se=NULL, key=FALSE){
    
    if(is.null(est) | is.null(se)){
        
        return(NULL) 
        
    }else{
        
        if(key){
            
            return(c(estimate   = exp(est),
                     se         = NA,
                     cv         = NA, 
                     l95        = exp(est - 1.96 * se), 
                     u95        = exp(est + 1.96 * se)))
            
        }else{
            
            return(c(estimate   = est,
                     se         = se,
                     cv         = se / est, 
                     l95        = est - 1.96 * se, 
                     u95        = est + 1.96 * se))
            
        }
        
    }
    
}

